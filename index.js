function cart(price) {
    var total = 0;
    var amount = parseInt(price);
    total += amount;
    var final = total.toString();
    var html = '<h3>Cart</h3>';
    html += '<p>Price = $' + final + '</p>';
    html += '<p> You have sucessfuly purchase this item</p>';
    $('#cart').attr('hidden', false);
    $('#cart').html(html);
}
